/*
Age: 25
Initial: A
Name: John
*/

public class hm_01 {
    public static void main(String[] args) {
        // 宣告並初始化一個整數變數 age
        int age = 25;

        // 宣告並初始化一個字元變數 initial
        char initial = 'A';

        // 宣告並初始化一個字串變數 name
        String name = "John";

        // 打印每個變數的值
        System.out.println("Age: " + age);
        System.out.println("Initial: " + initial);
        System.out.println("Name: " + name);
    }
}
